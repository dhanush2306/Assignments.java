public class Task3 {
    public static void main(String[] args) {
        double salary = 30000;
        salary += 5000;   
        System.out.println("After increment: " + salary);

  
        salary -= 2000;   
        System.out.println("After deduction: " + salary);

        
        salary *= 2;     
        System.out.println("After bonus: " + salary);

       
        salary /= 2;      
        System.out.println("After division: "+ salary);
    
    }
}


